# short and reverse method for list

numlist = [5,4,2,3,1]
numlist.sort(reverse=True) #sort as highest to lowest same as reverse
numlist.sort() #sort as numeric
print(f'sort: {numlist}')

pets = ['dog', 'cat']
pets.sort() #sort as aplhebet
print(f'sort: {pets}') 


